#include "../../includes/libft.h"
#include <stdlib.h>

/**
 * @brief free one element of a chained list
 * 
 * @param lst the element to free
 * @param del the function that free the content
 */
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst != NULL && del != NULL)
	{
		del(lst->content);
		free(lst);
	}
}
